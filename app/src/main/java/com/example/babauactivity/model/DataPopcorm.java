package com.example.babauactivity.model;

public class DataPopcorm {
    private int Hinhpopcorm;
    private String Tenpopcorm;

    public DataPopcorm(int hinhpopcorm, String tenpopcorm) {
        Hinhpopcorm = hinhpopcorm;
        Tenpopcorm = tenpopcorm;
    }

    public int getHinhpopcorm() {
        return Hinhpopcorm;
    }

    public void setHinhpopcorm(int hinhpopcorm) {
        Hinhpopcorm = hinhpopcorm;
    }

    public String getTenpopcorm() {
        return Tenpopcorm;
    }

    public void setTenpopcorm(String tenpopcorm) {
        Tenpopcorm = tenpopcorm;
    }
}
