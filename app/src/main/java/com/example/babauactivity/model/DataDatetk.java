package com.example.babauactivity.model;

public class DataDatetk {
    private String Stt;
    private String Datetk;

    public DataDatetk(String stt, String datetk) {
        Stt = stt;
        Datetk = datetk;
    }

    public String getStt() {
        return Stt;
    }

    public void setStt(String stt) {
        Stt = stt;
    }

    public String getDatetk() {
        return Datetk;
    }

    public void setDatetk(String datetk) {
        Datetk = datetk;
    }
}
